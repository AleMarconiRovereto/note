    public void updateComponenteListaRemindersModel() {
        jList1.setModel(new javax.swing.AbstractListModel<String>() {
            String[] strings = estraiArrayDiNomiDaNoteConAllert();

            @Override
            public int getSize() {
                return strings.length;
            }

            @Override
            public String getElementAt(int i) {
                return strings[i];
            }
        });
    }

    //prende solo i nomi delle note che hanno allert true
    public String[] estraiArrayDiNomiDaNoteConAllert() {
        int count = 0;
        for (int i = 0; i < listaNote.listaNote.size(); i++) {
            Nota nota = listaNote.listaNote.get(i);
            if (nota instanceof NotaConAllert) {
                if (((NotaConAllert) nota).isAllertAttivo()) {
                    count++;
                }
            }
        }

        String[] strings = new String[count];
        int index = 0;
        for (int i = 0; i < listaNote.listaNote.size(); i++) {
            Nota nota = listaNote.listaNote.get(i);
            if (nota instanceof NotaConAllert) {
                if (((NotaConAllert) nota).isAllertAttivo()) {
                    strings[index] = nota.titolo;
                    index++;
                }
            }
        }
        return strings;
    }

  
