    // gestisce il click sui valori della lista par aprire notaAperta
    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {
        Object valoreSelezionato = jList1.getSelectedValue();

        if (valoreSelezionato != null) {
            final int posizioneOggettoCorrente = listaNote.returnJsonPositionInList(valoreSelezionato.toString());

            java.awt.EventQueue.invokeLater(new Runnable() {
                public void run() {
                    PaginaNotaAperta pna = new PaginaNotaAperta(memoria, contestoPrincipale, listaNote,
                            posizioneOggettoCorrente,
                            PaginaReminders.this);
                    pna.setLocationRelativeTo(PaginaReminders.this);
                    java.awt.Point p = pna.getLocation();
                    pna.setLocation(p.x + 30, p.y + 30);
                    pna.setVisible(true);
                }
            });
        } else {
            JOptionPane.showMessageDialog(this, "Devi prima selezionare un elemento dalla lista.");
        }
    }// GEN-LAST:event_jList1MouseClicked

    // il bottone che dissattiva tutti gli allert attivi e chiude la pagina
    private void bottoneDisattivaAllertActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_bottoneDisattivaAllertActionPerformed
        for (int i = 0; i < listaNote.listaNote.size(); i++) {
            Nota nota = listaNote.listaNote.get(i);
            if (nota instanceof NotaConAllert) {
                NotaConAllert notaConAllert = (NotaConAllert) nota;
                if (notaConAllert.isAllertAttivo()) {
                    notaConAllert.setAllertAttivo(false);
                    try {
                        memoria.rimuoviDaCSV(nota);
                        memoria.salvaEControllaDuplicatiInCSV(nota);
                    } catch (Exception e) {
                        System.out.println(e);
                    }
                }
            }
        }
        contestoPrincipale.updateComponenteListaNoteModel();
        this.dispose();
    }

